package thuc_hanh;

public class ThucHanhKhaiBaoVaSuDungBien {
    public static void main(String[] args) { //psvm

        //Biến i kiểu số nguyên, có giá trị là 10
        //Biến f kiểu số thực, có giá trị là 20.5
        //Biến d kiểu số thực lớn, có giá trị là 20.5
        //Biến b kiểu logic, có giá trị là true
        //Biến c kiểu ký tự, có giá trị là 'a'
        //Biến s kiểu chuỗi, có giá trị là "Hà Nội".
        int soNguyen=10;
        System.out.println("số nguyên có giá trị:"+soNguyen); //sout
        float soThuc=20.5f;
        System.out.println("số thực:"+soThuc);
        double soThucLon=20.5;
        System.out.printf("số thực lớn"+soThucLon);
        boolean b=true;
        System.out.println("giá trị:"+b);
        char c='a';
        System.out.println("có kí tự là:" +c);
        String s="Hà Nội";
        System.out.println("chuỗi:"+s);

    }
}
